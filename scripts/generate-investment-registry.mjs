/**
 * Generates NEPSE equity + Nepal MF registry from local inputs:
 * - scripts/sharealpha-stock.md (Share Alpha stock directory markdown export)
 * - scripts/cdsc-mf.html (downloaded CDSC registered mutual funds page)
 *
 * Run: node scripts/generate-investment-registry.mjs
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, "..");

const SHAREALPHA = path.join(ROOT, "scripts/sharealpha-stock.md");
const CDSC_HTML = path.join(ROOT, "scripts/cdsc-mf.html");
const OUT_NEPSE = path.join(ROOT, "src/lib/investment-market/data/nepse/generated-equities.ts");
const OUT_MF = path.join(ROOT, "src/lib/investment-market/data/mutual-funds-cdsc.generated.ts");

/** @type {readonly string[]} — longest first for prefix match */
const EXTERNAL_SECTOR_PREFIXES = [
  "Manufacturing And Processing",
  "Manufacturing and Processing",
  "Commercial Banks",
  "Development Banks",
  "Non Life Insurance",
  "Life Insurance",
  "Hotels And Tourism",
  "Corporate Debenture",
  "Finance Companies",
  "Preferred Stock",
  "Promoter Share",
  "Microfinance",
  "Hydro Power",
  "Mutual Fund",
  "Mutal Fund",
  "Tradings",
  "Investment",
  "Manufacturing",
  "Finance",
  "Others",
].sort((a, b) => b.length - a.length);

/** Map external directory sector → app `NepseSectorName` */
function mapSector(external) {
  switch (external) {
    case "Commercial Banks":
      return "Commercial Banks";
    case "Development Banks":
      return "Development Banks";
    case "Finance":
    case "Finance Companies":
      return "Finance";
    case "Hydro Power":
      return "Hydropower";
    case "Life Insurance":
    case "Non Life Insurance":
      return "Insurance";
    case "Microfinance":
      return "Microfinance";
    case "Hotels And Tourism":
      return "Hotels";
    case "Manufacturing":
    case "Manufacturing and Processing":
    case "Manufacturing And Processing":
      return "Manufacturing";
    case "Tradings":
      return "Trading";
    case "Investment":
      return "Investment";
    case "Others":
      return "Investment";
    default:
      return null;
  }
}

const EXCLUDE_EXTERNAL = new Set([
  "Mutual Fund",
  "Mutal Fund",
  "Corporate Debenture",
  "Preferred Stock",
  "Promoter Share",
]);

function stripTitle(s) {
  return s.replace(/\s+/g, " ").trim();
}

function parseShareAlpha(md) {
  const re = /### ([A-Z0-9]+)\s*\n\n\[([^\]]+?)CLOSE PRICE([^\]]*)\]/g;
  const rows = [];
  let m;
  while ((m = re.exec(md))) {
    const symbol = m[1];
    const inner = m[2];
    const priceTail = m[3] || "";
    let ext = null;
    let rest = inner;
    for (const p of EXTERNAL_SECTOR_PREFIXES) {
      if (rest.startsWith(p)) {
        ext = p;
        rest = rest.slice(p.length);
        break;
      }
    }
    rows.push({ symbol, externalSector: ext, companyName: stripTitle(rest), priceTail });
  }
  return rows;
}

function parseClosePrice(tail) {
  const t = tail.trim();
  if (t.startsWith("---")) return 100;
  const n = parseFloat(t.replace(/,/g, ""));
  return Number.isFinite(n) && n > 0 ? n : 100;
}

function shouldSkipEquity(r) {
  if (!r.externalSector) return true;
  if (EXCLUDE_EXTERNAL.has(r.externalSector)) return true;
  if (/^\d+$/.test(r.symbol)) return true;
  /** Debentures / structured symbols (e.g. ADBLD83) are not common equity. */
  if (/[0-9]/.test(r.symbol)) return true;
  const name = r.companyName.toLowerCase();
  if (name.includes("debenture") || name.includes("%")) return true;
  if (/\bpromoter\b/i.test(r.companyName)) return true;
  if (/PO$/.test(r.symbol) && r.externalSector === "Finance") return true;
  return false;
}

function escapeTs(s) {
  return s.replace(/\\/g, "\\\\").replace(/'/g, "\\'");
}

function emitNepse(rows, cdscClosedScrips) {
  const seen = new Set();
  const lines = [];
  lines.push(`/** Auto-generated by scripts/generate-investment-registry.mjs — do not edit by hand. */`);
  lines.push(`import { nepseStock } from "@/lib/investment-market/data/nepse/helpers";`);
  lines.push(`import type { NepseListedStock } from "@/lib/investment-market/types";`);
  lines.push(``);
  lines.push(`export const NEPSE_GENERATED_EQUITIES: readonly NepseListedStock[] = [`);
  for (const r of rows) {
    if (shouldSkipEquity(r)) continue;
    const sector = mapSector(r.externalSector);
    if (!sector) continue;
    if (cdscClosedScrips.has(r.symbol)) continue;
    if (seen.has(r.symbol)) continue;
    seen.add(r.symbol);
    const price = parseClosePrice(r.priceTail);
    lines.push(
      `  nepseStock("${escapeTs(r.symbol)}", "${escapeTs(r.companyName)}", "${sector}", [], ${Math.round(price * 100) / 100}),`,
    );
  }
  lines.push(`];`);
  lines.push(``);
  lines.push(`export const NEPSE_GENERATED_EQUITY_COUNT = ${seen.size} as const;`);
  lines.push(``);
  return lines.join("\n");
}

function parseCdscRows(html) {
  const body = html.split("<tbody>")[1]?.split("</tbody>")[0] ?? "";
  const trs = body.split("<tr>");
  const out = [];
  for (const chunk of trs) {
    const tds = [...chunk.matchAll(/<td[^>]*>([\s\S]*?)<\/td>/g)].map((x) =>
      stripTitle(x[1].replace(/<[^>]+>/g, " ")),
    );
    if (tds.length < 8) continue;
    const sn = tds[0];
    if (!/^\d+$/.test(sn)) continue;
    out.push({
      sn,
      description: tds[1],
      scrip: tds[2],
      isinType: tds[4].toUpperCase(),
      rta: tds[7],
    });
  }
  return out;
}

function titleManager(rta) {
  return stripTitle(rta)
    .split(/\s+/)
    .map((raw) => {
      const w = raw.toLowerCase();
      if (w === "limited" || w === "ltd." || w === "ltd") return "Limited";
      if (w === "co." || w === "company") return raw.length <= 3 ? raw.toUpperCase() : raw.charAt(0) + raw.slice(1).toLowerCase();
      if (raw === raw.toUpperCase() && raw.length <= 5 && /^[A-Z0-9]+$/.test(raw)) return raw;
      return raw.charAt(0) + raw.slice(1).toLowerCase();
    })
    .join(" ");
}

function emitMf(rows) {
  const open = [];
  const closed = [];
  for (const r of rows) {
    const t = r.isinType;
    if (t.includes("OTHER")) continue;
    if (t.includes("OPEN ENDED")) {
      open.push(r);
    } else if (t.includes("CLOSED ENDED")) {
      closed.push(r);
    }
  }
  const uniq = (arr, keyFn) => {
    const m = new Map();
    for (const x of arr) m.set(keyFn(x), x);
    return [...m.values()];
  };
  const openU = uniq(open, (r) => r.scrip);
  const closedU = uniq(closed, (r) => r.scrip);

  const lines = [];
  lines.push(`/** Auto-generated from CDSC registered mutual funds (scripts/cdsc-mf.html). */`);
  lines.push(`import type { ClosedEndedMutualFund, OpenEndedMutualFund } from "@/lib/investment-market/types";`);
  lines.push(``);
  lines.push(`function openMf(`);
  lines.push(`  key: string,`);
  lines.push(`  fundName: string,`);
  lines.push(`  fundManager: string,`);
  lines.push(`  category: string,`);
  lines.push(`  sipSupported: boolean,`);
  lines.push(`  demoNavNpr: number,`);
  lines.push(`): OpenEndedMutualFund {`);
  lines.push(
    `  return { universe: "open_end_mf", key, fundName, fundManager, category, sipSupported, demoNavNpr };`,
  );
  lines.push(`}`);
  lines.push(``);
  lines.push(`function closedMf(`);
  lines.push(`  key: string,`);
  lines.push(`  ticker: string,`);
  lines.push(`  fundName: string,`);
  lines.push(`  category: string,`);
  lines.push(`  demoLastPriceNpr: number,`);
  lines.push(`): ClosedEndedMutualFund {`);
  lines.push(
    `  return { universe: "closed_end_mf", key, ticker, fundName, category, demoLastPriceNpr };`,
  );
  lines.push(`}`);
  lines.push(``);
  lines.push(`export const CDSC_OPEN_ENDED_FUNDS: readonly OpenEndedMutualFund[] = [`);
  for (const r of openU) {
    // CDSC does not publish SIP flags; open-ended schemes in Nepal commonly offer SIP — default true (verify with RTA).
    const sip = true;
    lines.push(
      `  openMf("open_end_mf:${escapeTs(r.scrip)}", "${escapeTs(r.description)}", "${escapeTs(titleManager(r.rta))}", "Open-ended mutual fund", ${sip ? "true" : "false"}, 100),`,
    );
  }
  lines.push(`];`);
  lines.push(``);
  lines.push(`export const CDSC_CLOSED_ENDED_FUNDS: readonly ClosedEndedMutualFund[] = [`);
  for (const r of closedU) {
    lines.push(
      `  closedMf("closed_end_mf:${escapeTs(r.scrip)}", "${escapeTs(r.scrip)}", "${escapeTs(r.description)}", "Closed-ended mutual fund", 100),`,
    );
  }
  lines.push(`];`);
  lines.push(``);
  lines.push(`export const CDSC_OPEN_MF_COUNT = ${openU.length} as const;`);
  lines.push(`export const CDSC_CLOSED_MF_COUNT = ${closedU.length} as const;`);
  lines.push(``);
  return lines.join("\n");
}

function main() {
  if (!fs.existsSync(SHAREALPHA)) {
    console.error("Missing", SHAREALPHA);
    process.exit(1);
  }
  if (!fs.existsSync(CDSC_HTML)) {
    console.error("Missing", CDSC_HTML, "(curl https://cdsc.com.np/mutualfunds)");
    process.exit(1);
  }
  const md = fs.readFileSync(SHAREALPHA, "utf8");
  const html = fs.readFileSync(CDSC_HTML, "utf8");
  const sa = parseShareAlpha(md);
  const cdsc = parseCdscRows(html);
  const cdscClosedScrips = new Set(
    cdsc.filter((r) => r.isinType.includes("CLOSED ENDED")).map((r) => r.scrip),
  );
  fs.writeFileSync(OUT_NEPSE, emitNepse(sa, cdscClosedScrips));
  fs.writeFileSync(OUT_MF, emitMf(cdsc));
  console.log("ShareAlpha parsed rows:", sa.length);
  console.log("CDSC table rows:", cdsc.length);
  console.log("Wrote", path.relative(ROOT, OUT_NEPSE), "(see NEPSE_GENERATED_EQUITY_COUNT inside)");
  console.log("Wrote", path.relative(ROOT, OUT_MF));
}

main();
