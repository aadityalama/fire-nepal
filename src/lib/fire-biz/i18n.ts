export function fireBizT(locale: FireBizLocale, section: keyof typeof FIRE_BIZ_I18N.en, key?: string): string {
  const dict = FIRE_BIZ_I18N[locale][section];
  if (typeof dict === "string") return dict;
  if (key && key in dict) return (dict as Record<string, string>)[key];
  return key ?? "";
}

export function formatBizNpr(amount: number): string {
  return `NPR ${amount.toLocaleString("en-NP", { maximumFractionDigits: 0 })}`;
}
