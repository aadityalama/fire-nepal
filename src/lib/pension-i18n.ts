const MAP: Record<PensionLocale, Record<string, string>> = { en: EN, ko: KO, ne: NE };

export function pensionT(locale: PensionLocale, key: string, vars?: Vars): string {
  const table = MAP[locale] ?? EN;
  let s = table[key] ?? EN[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      s = s.replaceAll(`{${k}}`, String(v));
    }
  }
  return s;
}
